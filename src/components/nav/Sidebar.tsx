export default function Sidebar({ lang, userName, userRole, logoutAction, nav }: Props) {
  const pathname = usePathname();

  const items = [
    { href: `/${lang}/today`, label: nav.today, icon: TODAY_ICON },
    { href: `/${lang}/tasks`, label: nav.tasks, icon: TASK_ICON },
    { href: `/${lang}/spaces`, label: nav.spaces, icon: SPACES_ICON },
    { href: `/${lang}/schedule`, label: nav.schedule, icon: SCHEDULE_ICON },
    ...(userRole === "owner" ? [{ href: `/${lang}/family`, label: nav.family, icon: FAMILY_ICON }] : []),
    { href: `/${lang}/settings`, label: nav.settings, icon: SETTINGS_ICON },
  ];

  return (
    <aside className="hidden md:flex md:w-56 md:flex-col md:fixed md:inset-y-0 border-r border-amber-200 bg-amber-50 dark:border-amber-900 dark:bg-stone-900">
      <div className="flex h-16 items-center px-6 border-b border-amber-200 dark:border-amber-900">
        <span className="text-lg font-bold text-amber-600 dark:text-amber-400">OurWay</span>
      </div>

      <nav className="flex-1 px-3 py-4 space-y-1">
        {items.map((item) => {
          const active = pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 rounded-xl px-3 py-2.5 text-sm font-medium transition-colors ${
                active
                  ? "bg-amber-500 text-white"
                  : "text-stone-600 hover:bg-amber-100 dark:text-stone-300 dark:hover:bg-stone-800"
              }`}
            >
              {item.icon}
              {item.label}
            </Link>
          );
        })}
      </nav>

      <div className="px-3 py-4 border-t border-amber-200 dark:border-amber-900">
        <div className="px-3 mb-2 text-sm font-medium text-stone-600 dark:text-stone-300 truncate">
          {userName}
        </div>
        <form action={logoutAction}>
          <button
            type="submit"
            className="w-full rounded-xl px-3 py-2 text-left text-sm text-stone-500 hover:bg-amber-100 dark:text-stone-400 dark:hover:bg-stone-800"
          >
            {nav.settings === "Налаштування" ? "Вийти" : "Logout"}
          </button>
        </form>
      </div>
    </aside>
  );
}
